@app.route('/register', methods=['POST', 'GET'])
def register():
    form = RegisterNewUserForm()
    if request.method == 'POST':
        if form.validate_on_submit():
            email = request.form.get('email')
            password = request.form.get('password')
            name = request.form.get('name')
            hashed_salt_password = generate_password_hash(password=password, method='pbkdf2:sha256',
                                                          salt_length=8)
            new_user = User(
                    name=name,
                    email=email,
                    password=hashed_salt_password
            )
            if db.session.query(User).filter_by(email=email).first() is None:
                db.session.add(new_user)
                db.session.commit()
                login_user(new_user)
                return redirect(url_for("get_all_posts"))
            else:
                flash("Email already exists!")
                return redirect((url_for('login')))
    return render_template("register.html", form=form)


@app.route('/login', methods=['POST', 'GET'])
def login():
    form = LoginForm()
    if request.method == 'POST':
        email = request.form.get("email"),
        password = request.form.get("password")
        user = db.session.query(User).filter_by(email=email[0]).first()
        if not user:
            flash("That email does not exist, please try again.")
            return redirect(url_for('login'))
            # Password incorrect
        elif not check_password_hash(user.password, password):
            flash('Password incorrect, please try again.')
            return redirect(url_for('login'))
            # Email exists and password correct
        else:
            login_user(user)
            return redirect(url_for('get_all_posts'))
    return render_template("login.html", form=form)


